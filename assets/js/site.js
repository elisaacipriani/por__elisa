function ensureInkFilter() {
  if (document.getElementById("ink-filter-svg")) {
    return;
  }

  document.body.insertAdjacentHTML(
    "afterbegin",
    `<svg id="ink-filter-svg" class="ink-filter-svg" aria-hidden="true" focusable="false">
      <filter id="ink-spread" x="-4%" y="-10%" width="108%" height="120%">
        <feTurbulence type="fractalNoise" baseFrequency="0.72 0.18" numOctaves="2" seed="11" result="paperNoise"></feTurbulence>
        <feDisplacementMap in="SourceGraphic" in2="paperNoise" scale="0.34" xChannelSelector="R" yChannelSelector="G" result="softRipple"></feDisplacementMap>
        <feGaussianBlur in="softRipple" stdDeviation="0.06" result="softInk"></feGaussianBlur>
        <feComponentTransfer in="softInk">
          <feFuncA type="table" tableValues="0 0.92 1"></feFuncA>
        </feComponentTransfer>
      </filter>
    </svg>`
  );
}

const savedLanguage = localStorage.getItem("por-elisa-language");
let currentLanguage = savedLanguage || "es";

function setLanguage(language) {
  currentLanguage = copy[language] ? language : "es";
  document.documentElement.lang = currentLanguage;
  localStorage.setItem("por-elisa-language", currentLanguage);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (copy[currentLanguage][key]) {
      if (copy[currentLanguage][key].includes("<br>")) {
        node.innerHTML = copy[currentLanguage][key];
      } else {
        node.textContent = copy[currentLanguage][key];
      }
    }
  });

  document.querySelectorAll("[data-set-lang]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.setLang === currentLanguage));
  });
}

function setupFilters() {
  document.querySelectorAll("[data-filter]").forEach((button) => {
    button.addEventListener("click", () => {
      const filter = button.dataset.filter;

      document.querySelectorAll("[data-filter]").forEach((item) => {
        item.classList.toggle("is-active", item === button);
      });

      document.querySelectorAll("[data-category]").forEach((item) => {
        const categories = item.dataset.category.split(" ");
        item.hidden = filter !== "all" && !categories.includes(filter);
      });
    });
  });
}

function setupSequences() {
  document.querySelectorAll("[data-sequence]").forEach((sequence) => {
    const image = sequence.querySelector("img");
    const frames = (sequence.dataset.frames || "")
      .split(",")
      .map((frame) => frame.trim())
      .filter(Boolean);
    const interval = Number(sequence.dataset.interval) || 300;

    if (!image || frames.length < 2) {
      return;
    }

    let index = 0;
    frames.slice(1).forEach((frame) => {
      const preload = new Image();
      preload.src = frame;
    });

    window.setInterval(() => {
      index = (index + 1) % frames.length;
      image.src = frames[index];
    }, interval);
  });
}

document.querySelectorAll("[data-set-lang]").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.setLang));
});

ensureInkFilter();
setupFilters();
setupSequences();
setLanguage(currentLanguage);
